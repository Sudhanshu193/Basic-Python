print (len(A))
print(A[4:20]) #here we slice the sentence from index 4 to index 19
# (index 20 does not include)

#slicing from end
print(A[:30])#here index till index 30 it works.

#slicing from start
print(A[17:])#here it goes to end

#negative indexing
print(A[-20:-2])# it start from last index